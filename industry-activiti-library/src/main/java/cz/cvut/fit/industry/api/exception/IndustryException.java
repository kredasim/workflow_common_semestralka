package cz.cvut.fit.industry.api.exception;

public class IndustryException extends RuntimeException {

	public IndustryException() {
	}

	public IndustryException(String arg0) {
		super(arg0);
	}

	public IndustryException(Throwable arg0) {
		super(arg0);
	}

	public IndustryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}


}


